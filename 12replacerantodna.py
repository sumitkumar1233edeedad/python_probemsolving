n = 'TTTT'
"""
This script replaces all occurrences of the character 'T' with 'U' in a given DNA string.
It demonstrates the use of the `replace` method in Python for string manipulation.

Variables:
- n (str): A string representing a DNA sequence.

Functionality:
- Converts the DNA sequence (containing 'T') into an RNA sequence (containing 'U').

Example:
Input: 'TTTT'
Output: 'UUUU'
"""
# This script is useful for basic DNA to RNA transcription.
print(n.replace('T',"U"))